@ID_DEPARTAMENTO	AS VARCHAR(2) 
AS

SELECT	ID_UBIGEO, 
		PROVINCIA FROM UBIGEO 
WHERE	SUBSTRING(ID_UBIGEO,5,2) = '00' 
		AND SUBSTRING(ID_UBIGEO,1,2) = @ID_DEPARTAMENTO 
		AND SUBSTRING(ID_UBIGEO,3,2) <> '00' 
		AND ESTADO = 'A'
GO

