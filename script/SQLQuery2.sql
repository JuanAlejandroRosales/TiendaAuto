DROP PROC USP_PAGINACION_AUTOMOVIL
GO

CREATE PROCEDURE USP_PAGINACION_AUTOMOVIL
	@NUMPAG	AS INT,
	@FILPAG	AS INT
AS
SELECT * FROM
AUTOMOVIL
WHERE ESTADO = 'A'
ORDER BY ID_AUTOMOVIL ASC
OFFSET @FILPAG*(@NUMPAG-1) ROWS
FETCH NEXT @FILPAG ROWS ONLY;
GO

