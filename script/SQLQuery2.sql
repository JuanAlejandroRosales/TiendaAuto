CREATE PROCEDURE USP_FILTRAR_CLIENTE
		@NOMBRES_RAZSOC VARCHAR(100) 
AS
SELECT	@NOMBRES_RAZSOC = '%'+ LTRIM(RTRIM(@NOMBRES_RAZSOC)) + '%';
SELECT	ID_CLIENTE, 
		PRI_NOMBRE+' '+SEG_NOMBRE+' '+PRI_APELLIDO+' '+SEG_APELLIDO AS NOMBRES_RAZSOC,
		NUMERO_DOCUMENTO AS DNIRUC,
		DIRECCION
FROM CLIENTE
WHERE TIPO_CLIENTE='NATURAL' AND PRI_NOMBRE+' '+SEG_NOMBRE+' '+PRI_APELLIDO+' '+SEG_APELLIDO LIKE @NOMBRES_RAZSOC
UNION ALL
SELECT	ID_CLIENTE, 
		RAZON_SOCIAL,
		NUMERO_RUC,
		DIRECCION 
FROM CLIENTE
WHERE TIPO_CLIENTE='JURIDICO' AND RAZON_SOCIAL LIKE @NOMBRES_RAZSOC
GO

