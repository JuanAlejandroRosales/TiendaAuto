CREATE PROCEDURE USP_UPDATE_AUTOMOVIL
	@ID_AUTOMOVIL	AS VARCHAR(9),
	@ID_MARCA		AS VARCHAR(9),
	@MODELO			AS VARCHAR(9),
	@COLOR			AS VARCHAR(32),
	@PRECIO			AS DECIMAL(15,2),
	@STOCK			AS INT
AS
UPDATE AUTOMOVIL
   SET	ID_MARCA = @ID_MARCA,
		MODELO = @MODELO,
		COLOR = @COLOR,
		PRECIO = @PRECIO,
		STOCK = @STOCK
WHERE ID_AUTOMOVIL=@ID_AUTOMOVIL
GO


